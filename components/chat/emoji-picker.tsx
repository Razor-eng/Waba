interface EmojiPickerProps {
  onEmojiSelect: (emoji: string) => void;
}

export function EmojiPicker({ onEmojiSelect }: EmojiPickerProps) {
  const [selectedCategory, setSelectedCategory] =
    useState<keyof typeof emojiCategories>("Smileys & People");
  const [searchQuery, setSearchQuery] = useState("");

  const filteredEmojis = searchQuery
    ? Object.values(emojiCategories)
        .flatMap((category) => category.emojis)
        .filter((emoji) => {
          // Simple search by emoji or common keywords
          const keywords = {
            "ðŸ˜€": ["happy", "smile", "joy"],
            "ðŸ˜‚": ["laugh", "funny", "lol"],
            "â¤ï¸": ["love", "heart", "red"],
            "ðŸ‘": ["thumbs", "up", "good", "like"],
            "ðŸ”¥": ["fire", "hot", "lit"],
            "ðŸ’¯": ["hundred", "perfect", "score"],
            "ðŸŽ‰": ["party", "celebration", "confetti"],
            "ðŸš€": ["rocket", "space", "launch"],
            "ðŸ’Ž": ["diamond", "gem", "precious"],
            "â­": ["star", "favorite", "rating"],
          };

          const emojiKeywords = keywords[emoji as keyof typeof keywords] || [];
          return (
            emojiKeywords.some((keyword) =>
              keyword.toLowerCase().includes(searchQuery.toLowerCase())
            ) || emoji.includes(searchQuery)
          );
        })
    : emojiCategories[selectedCategory].emojis;

  return (
    <Popover>
      <PopoverTrigger asChild>
        <Button variant="ghost" size="icon" className="h-8 w-8">
          <Smile className="w-4 h-4" />
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-96 p-0" align="end">
        <div className="p-3 border-b">
          <div className="relative">
            <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 w-4 h-4 text-gray-400" />
            <Input
              placeholder="Search emojis..."
              value={searchQuery}
              onChange={(e) => setSearchQuery(e.target.value)}
              className="pl-10 h-9"
            />
          </div>
        </div>

        {!searchQuery && (
          <div className="border-b bg-gray-50">
            <ScrollArea className="w-full">
              <div className="flex p-1">
                {Object.entries(emojiCategories).map(([category, data]) => (
                  <Button
                    key={category}
                    variant={
                      selectedCategory === category ? "secondary" : "ghost"
                    }
                    size="sm"
                    className="flex-shrink-0 px-3 py-2 mx-1 rounded-md"
                    onClick={() =>
                      setSelectedCategory(
                        category as keyof typeof emojiCategories
                      )
                    }
                    title={category}
                  >
                    <span className="text-lg">{data.icon}</span>
                  </Button>
                ))}
              </div>
            </ScrollArea>
          </div>
        )}

        <ScrollArea className="h-64">
          <div className="p-3">
            {!searchQuery && (
              <h3 className="text-sm font-medium text-gray-700 mb-2">
                {selectedCategory}
              </h3>
            )}
            <div className="grid grid-cols-8 gap-1">
              {filteredEmojis.map((emoji, index) => (
                <Button
                  key={`${emoji}-${index}`}
                  variant="ghost"
                  size="sm"
                  className="h-10 w-10 p-0 hover:bg-blue-50 hover:scale-110 transition-all duration-150 text-lg"
                  onClick={() => onEmojiSelect(emoji)}
                  title={emoji}
                >
                  {emoji}
                </Button>
              ))}
            </div>
            {filteredEmojis.length === 0 && searchQuery && (
              <div className="text-center py-8 text-gray-500">
                <p>No emojis found for "{searchQuery}"</p>
                <p className="text-sm mt-1">
                  Try searching for "happy", "love", or "fire"
                </p>
              </div>
            )}
          </div>
        </ScrollArea>

        <div className="border-t p-2 bg-gray-50">
          <p className="text-xs text-gray-500 text-center">
            {Object.values(emojiCategories).reduce(
              (total, category) => total + category.emojis.length,
              0
            )}
            + emojis available
          </p>
        </div>
      </PopoverContent>
    </Popover>
  );
}
